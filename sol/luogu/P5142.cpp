using L = LazySegmentTree<Info, Tag>;

int main() {
    cin.tie(nullptr)->sync_with_stdio(false);
    
    int n, m;
    cin >> n >> m;

    vector<Info> init(n);
    for (int i = 0; i < n; i++) {
        cin >> init[i].sum;
        init[i].act = 1;
        init[i].sqr = init[i].sum * init[i].sum;
    }

    auto D = [](const Info &v) -> Z {
        Z act = v.act;
        Z avg = v.sum * act.inv();
        return v.sqr * act.inv() - avg * avg;
    };

    L l(init);
    while (m--) {
        int op, x, y;
        cin >> op >> x >> y;
        x--;
        if (op == 1) {
            Info c;
            c.act = 1;
            c.sum = y;
            c.sqr = 1ll * y * y;
            l.modify(x, c);
        } else {
            cout << D(l.rangeQuery(x, y)) << "\n";
        }
    }
    
    return 0;
}